# Numeric types
i = 42          # int
f = 3.14        # float
c = 3 + 4j      # complex

# Sequence types
s = "Hello"     # str
l = [1, 2, 3]   # list
t = (1, 2, 3)   # tuple
r = range(5)    # range

# Set types
se = {1, 2, 3}  # set
fs = frozenset([1, 2, 3])  # frozenset

# Mapping type
d = {"a": 1, "b": 2}  # dict

# Boolean type
b = True       # bool

# None type
n = None       # NoneType

# Binary types
ba = bytearray(b"hello")  # bytearray
b = b"hello"             # bytes
mv = memoryview(b)       # memoryview

